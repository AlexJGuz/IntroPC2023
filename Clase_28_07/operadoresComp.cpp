#include <bits/stdc++.h>

using namespace std;

int main(){
    // Arimeticos
    int a = 1 , b= 2;
    // Iguales (==)
    bool f = a == b;
    cout << f << '\n';

    // Desigual
    f = a != b;
    cout << f << '\n';
    // Mayor
    f = a > b;
    cout << f << '\n';

    // Menor
    f = a < b;
    cout << f << '\n';

    // Mayor Iguales
    f = a >= b;
    cout << f << '\n';

    // Menor Iguales
    f = a <= b;
    cout << f << '\n';
}
