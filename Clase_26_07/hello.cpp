// Este header incluye todas las funciones, constantes
// y estructuras/tipos de datos de la libreria estándar de C++
#include <bits/stdc++.h>

// Esta linea te dejar hacer uso directo
// de la libreria estándar sin necesidad de 
// poner std antes de cualquier miembro de la libreria
// i.e., `std::cout` cambia a solamente `cout`
using namespace std;

// Todo programa en C++ debe de tener una función `main`
// que regrese un entero (por eso es `int main(){}`) y es
// la función que se va a ejecutar al correr el programa
int main() {
    cout << "Hola Mundo\n";
    // En C++ es opcional poner explicitamente return 0,
    // lo que hace esto es indicar que el programa terminó
    // exitosamente, si se omite por defecto regresa 0
    return 0;
}
